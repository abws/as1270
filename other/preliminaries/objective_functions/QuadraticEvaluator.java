package other.preliminaries.objective_functions;

/**
 * Quadratic Evaluator
 * Evaluation function that squares input
 * f(x) = x^2
 * @author Abdiwahab Salah
 * @version 06.12.22
 */
public class QuadraticEvaluator {
    public static long quadraticEvaluator(long input) {
        long output = input * input;
        return output;
    }
}
