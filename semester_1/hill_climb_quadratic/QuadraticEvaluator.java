package semester_1.hill_climb_quadratic;

/**
 * Quadratic Evaluator
 * Evaluation function that squares input
 * f(x) = x^2
 * @author Abdiwahab Salah
 * @version 06.12.22
 */
public class QuadraticEvaluator {
    public static int quadraticEvaluator(int input) {
        int output = input * input;
        return output;
    }

    
}
